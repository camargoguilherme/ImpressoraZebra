    // O metodo verifica se existe impressora conectada e a  
    // define como padrao.  
    public int detectaImpressoras() {
        PrintService service = PrintServiceLookup.lookupDefaultPrintService();
        int ind = 0;
        try {

            DocFlavor df = DocFlavor.SERVICE_FORMATTED.PRINTABLE;
            PrintService[] ps = PrintServiceLookup.lookupPrintServices(df, null);
            impressora = ps;
            for (int i = 0; i < impressora.length; i++) {
                if (service.getName().equalsIgnoreCase(impressora[i].getName())) {
                    ind = i;
                }
                listaimpressora.addItem(impressora[i].getName());
            }

        } catch (Exception e) {

            e.printStackTrace();

        }
        return ind;

    }

