    private void txtcodigoeanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtcodigoeanActionPerformed
        // TODO add your handling code here:
        txtpreco.requestFocus();

        try {

            String path = new TestePathDaClasse().pathDaClasse(TestePathDaClasse.class) + "Images/" + txtcodigoean.getText() + ".jpg";
            path = path.replace('/', '\\');

            ImageIcon icon = new ImageIcon(path);
            icon.setImage(icon.getImage().getScaledInstance(lblImagem.getWidth(), lblImagem.getHeight(), Image.SCALE_DEFAULT));
            lblImagem.setIcon(icon);

        } catch (Exception ex) {
            Logger.getLogger(HomeScreen.class.getName()).log(Level.SEVERE, null, ex);
        }
        txtcodigoean.setText(txtcodigoean.getText().replace(" ", ""));
        ProdutoDAO prod = new ProdutoDAO();
        if (txtcodigoean.getText() != null || txtcodigoean.getText() != "") {
            try {

                produto = prod.getProduto(txtcodigoean.getText());
            } catch (SQLException ex) {
                Logger.getLogger(HomeScreen.class.getName()).log(Level.SEVERE, null, ex);
            }
            lbldescricao.setText(produto.getDescricao());
            txtpreco.setText(produto.getPreco().replace(".", ","));

        } else {
            lbldescricao.setText("Produto nao Encontrado");
            txtpreco.setText("0,00");
        }

    }//GEN-LAST:event_txtcodigoeanActionPerformed

