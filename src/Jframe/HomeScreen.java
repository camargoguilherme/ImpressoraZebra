    private void btnImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImprimirActionPerformed
        // TODO add your handling code here:

        produto.setFacebook(txtFacebook.getText());
        produto.setInstagram(txtInstagram.getText());
        produto.setWhatsapp(txtWhatapp.getText());

        int ind = listaimpressora.getSelectedIndex();
        PrintService sp = impressora[ind];
        if (Double.parseDouble(txtpreco.getText().replace(",", ".")) > 0) {
            produto.imprimirEtiqueta(sp, txtqtdetiqueta.getText());
            JOptionPane meuJOPane = new JOptionPane("Imprimindo....");//instanciando o JOptionPane
            final JDialog dialog = meuJOPane.createDialog(null, "Impressao de Etiquetas");//aqui uso um JDialog para manipular
            //meu JOptionPane
            dialog.setModal(true);
            //Usando o javax.swing.Timer para poder gerar um evento em um tempo determinado
            //Veja o construtor da classe Timer para mais explicações
            Timer timer = new Timer(2 * 1000, new ActionListener() {
                public void actionPerformed(ActionEvent ev) {
                    dialog.dispose();  //o evento(no caso fechar o meu JDialog)
                }
            });
            txtpreco.setText(txtpreco.getText().replace(".", ","));
            timer.start();
            dialog.setVisible(true);
            timer.stop();

        } else {
            lbldescricao.setText("Produto nao Encontrado");
            txtpreco.setText("0,00");
            String message = "Deseja inserir um PREÇO\n manualmente para imprimir?";
            String title = "Produtos nao Encontrado";
            int reply = JOptionPane.showConfirmDialog(null, message, title, JOptionPane.YES_NO_OPTION);
            if (reply == JOptionPane.YES_OPTION) {
                txtpreco.requestFocus();
                txtpreco.setText("");
            } else {
                txtcodigoean.requestFocus();

            }
        }
    }//GEN-LAST:event_btnImprimirActionPerformed

